class People():
    def __init__(self,name,lastname):
        self.__name=name
        self.__lastname=lastname


    def get_name(self):
        return self.__name


    def get_lastname(self):
        return self.__lastname


    def set_name(self,name):
        self.__name=name

        
    def set_lastname(self,lastname):
        self.__lastname=lastname    
                

